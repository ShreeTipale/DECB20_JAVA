package Varible_DT;

public class Mathematical_Operation
{
public static void main(String[] args) 
{
	
	//variable declaration & initialization at a time
	
	int a=32;
	int b=32;
	int sum=a+b;
	int c=10;
	int d=4;
	int diff=c-d;
	byte e=127;
	byte f=127;
	int mul=e*f;
	
	System.out.println("32+32="+sum);
	System.out.println("10-4="+diff);
	System.out.println("127*127="+mul);
}
}
